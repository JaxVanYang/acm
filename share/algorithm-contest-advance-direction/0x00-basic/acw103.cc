#include <iostream>
using namespace std;
const int maxn = 2e5 + 5;
int a[maxn], b[maxn], c[maxn];

int main() {
    int n;
    scanf("%d", &n);
    for (int i = 0; i < n; ++i) scanf("%d", a + i);
    int m;
    scanf("%d", &m);
    for (int i = 0; i < m; ++i) scanf("%d", b + i);
    for (int i = 0; i < m; ++i) scanf("%d", c + i);

    
}